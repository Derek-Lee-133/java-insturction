package lee.business;

public class Country {
	private String country;
	public Country() {
		country = "";
	}

	public Country(String country) {
		super();
		this.country = country;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}
	
	

}
